package basic.ch04;

public class ForTest1 {

	public static void main(String[] args) {

		
		// 문제 - 화면에 0부터 9까지 화면에 출력 하시오.


		// 반복적인 패턴이 보인다면 반복문을 생각하자.
		// 0 ~ 9
		for(int i = 0; i < 10; i++) {
			
			// 반복적인 패턴에 구문을 작성한다.
			// i = 0;
			// i = 2;
			System.out.println(i);
			
		} // end of for
		
		
		
	}// end of main

}// end of class
